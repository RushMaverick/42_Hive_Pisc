#include <unistd.h>
#include <stdio.h>

char	*ft_strcpy(char *dest, char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char test[10];

	printf("%s", ft_strcpy(test, "asdasd"));
	return (0);
}
